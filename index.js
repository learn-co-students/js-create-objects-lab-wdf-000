'use strict';

function Dog(n,b,a){
  this.name = n;
  this.breed = b;
  this.age = a;
}

function Cat(n,b,a){
  this.name = n;
  this.breed = b;
  this.age = a;
}

function Ferret(n,a,fr){
  this.name = n;
  this.age = a;
  this.foodRequirements = fr;
}

function Bird(n,b){
  this.name = n;
  this.breed = b;
}

function Fish(n,s,wc){
  this.name = n;
  this.species = s;
  this.waterConditions = wc;
}
