'use strict';

var Dog = function(name,breed,age){
  this.name = name;
  this.breed = breed;
  this.age = age;
};

var Cat = function(name,breed,age){
  this.name = name;
  this.breed = breed;
  this.age = age;
};

var Ferret = function(name,age,foodRequirements){
  this.name = name;
  this.age = age;
  this.foodRequirements = foodRequirements;
};

var Bird = function(name,breed){
  this.name = name;
  this.breed = breed;
};

var Fish = function(name,species,waterConditions){
  this.name = name;
  this.species = species;
  this.waterConditions = waterConditions;
};
